package poo.ejercicioPoo;

public class SmartDevice {

    String brand;
    String model;
    String color;
    double screenSize;
    boolean celular;
    String chargerType;

    public SmartDevice() {}

    public SmartDevice(String brand, String model, String color, double screenSize, boolean celular, String chargerType) {
        this.brand = brand;
        this.model = model;
        this.color = color;
        this.screenSize = screenSize;
        this.celular = celular;
        this.chargerType = chargerType;
    }
}
